package thelm.jaopca.custom.library;

import net.minecraft.block.material.MapColor;

public enum EnumMapColor {
	
	AIR, GRASS, SAND, CLOTH, TNT, ICE, IRON, FOLIAGE, SNOW, CLAY, DIRT, STONE, WATER, WOOD, QUARTZ, ADOBE, MAGENTA, LIGHT_BLUE,
	YELLOW, LIME, PINK, GRAY, SILVER, CYAN, PURPLE, BLUE, BROWN, GREEN, RED, BLACK, GOLD, DIAMOND, LAPIS, EMERALD, OBSIDIAN, NETHERRACK,
	WHITE_STAINED_HARDENED_CLAY, ORANGE_STAINED_HARDENED_CLAY, MAGENTA_STAINED_HARDENED_CLAY, LIGHT_BLUE_STAINED_HARDENED_CLAY,
	YELLOW_STAINED_HARDENED_CLAY, LIME_STAINED_HARDENED_CLAY, PINK_STAINED_HARDENED_CLAY, GRAY_STAINED_HARDENED_CLAY,
	SILVER_STAINED_HARDENED_CLAY, CYAN_STAINED_HARDENED_CLAY, PURPLE_STAINED_HARDENED_CLAY, BLUE_STAINED_HARDENED_CLAY,
	BROWN_STAINED_HARDENED_CLAY, GREEN_STAINED_HARDENED_CLAY, RED_STAINED_HARDENED_CLAY, BLACK_STAINED_HARDENED_CLAY;
	
	
	public static MapColor fromName(String name) {
		for(int i = 0; i < values().length; i++) {
			EnumMapColor mapColor = values()[i];
			if(mapColor.name().equalsIgnoreCase(name)) {
				return MapColor.COLORS[i];
			}
		}
		return MapColor.STONE;
	}
}
